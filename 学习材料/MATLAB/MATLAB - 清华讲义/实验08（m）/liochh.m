function [f,g]=liochh(x)
a=[1.25 8.75 0.5  5.75 3   7.25];
b=[1.25 0.75 4.75 5    6.5 7.75];
d=[3 5 4 7 6 11];e=[20,20];
%x(1:6)=(c11:c61),x(7:12)=(c12:c62),x(13:14)=(x1,y1),x(15:16)=(x2,y2),
f=x(1:12)*[sqrt((x(13)-a).^2+(x(14)-b).^2),sqrt((x(15)-a).^2+(x(16)-b).^2)]';
A=[eye(6),eye(6);ones(1,6),zeros(1,6);zeros(1,6),ones(1,6)];
g=A*x(1:12)'-[d,e]';

